import { useContext, useEffect, useState } from 'react'
import { signOut, useSession } from 'next-auth/react'
import Head from 'next/head'
import Link from 'next/link'
import { Store } from '../utils/Store'
import 'react-toastify/dist/ReactToastify.css'
import { ToastContainer } from 'react-toastify'
import Cookies from 'js-cookie'
import { Menu } from '@headlessui/react'

export default function Layout({ title, children }) {

  const { data: session, status } = useSession()

  const { state, dispatch } = useContext(Store)
  const { cart } = state
  const [cartItemsCount, setCartItemsCount] = useState(0)

  useEffect(() => {
    setCartItemsCount(cart.cartItems.reduce((a, c) => a + c.quantity, 0))
  }, [cart.cartItems])


  const logoutClickHandler = () => {
    Cookies.remove('cart')
    dispatch({ type: 'CART_RESET' })
    signOut({ callbackUrl: '/login' })
  }

  return (
    <>
      <Head>
        <title>{title ? title + ' ' + '-' + " " + 'Ecommerce' : 'Ecommerce - nextjs-mongodb-tailwind'}</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <ToastContainer position="bottom-center" limit={1} autoClose={1000} />
      <div className='flex min-h-screen flex-col justify-between'>
        <header>
          <nav className='flex h-12 px-4 items-center justify-between shadow-md'>
            <Link href='/'>
              <a className='text-lg font-bold text-primary'>ECommerce</a>
            </Link>
            <div>
              <Link href='/cart'><a className='p-2'>
                Cart
                {cartItemsCount > 0 && (
                  <span className='ml-1 py-1 px-2 text-xs font-bold rounded-full bg-primary text-white'>
                    {cartItemsCount}
                  </span>
                )}
              </a></Link>
              {status === 'loading'
                ? 'Loading'
                : session?.user
                  ? <Menu as='div' className='relative inline-block'>
                    <Menu.Button>{session.user.name}</Menu.Button>
                    <Menu.Items className='absolute right-0 w-56 origin-top-right shadow-lg bg-white z-10 rounded'>
                      <Menu.Item>
                        <a
                          className='flex p-2 hover:bg-grey-light hover:text-primary rounded'
                          href="/profile"
                        >
                          Profile
                        </a>
                      </Menu.Item>
                      <Menu.Item>
                        <a
                          className='flex p-2 hover:bg-grey-light hover:text-primary rounded'
                          href="/order-history"
                        >
                          Order History
                        </a>
                      </Menu.Item>
                      <Menu.Item>
                        <a
                          href='#'
                          className='flex p-2 hover:bg-grey-light hover:text-primary rounded'
                          onClick={logoutClickHandler}
                        >
                          Log Out
                        </a>
                      </Menu.Item>
                    </Menu.Items>
                  </Menu>
                  :
                  <Link href='/login'>
                    <a className='p-2'>
                      Login
                    </a>
                  </Link>
              }
            </div>
          </nav>
        </header>
        <main className='container m-auto mt-4'>{children}</main>
        <footer className='flex h-10 justify-center items-center shadow-inner text-sm'>Copyright &copy; 2022 ECommerce</footer>
      </div>
    </>
  )
}
